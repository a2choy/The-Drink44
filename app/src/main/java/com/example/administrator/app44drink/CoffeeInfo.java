package com.example.administrator.app44drink;

public class CoffeeInfo {
    int num;
    String img;
    String title;
    int banner;
    int km;
    double la;
    double lo;
    int myshop;

    public CoffeeInfo(int num, String img, String title, int banner, int km, double la, double lo, int myshop) {
        this.num = num;
        this.img = img;
        this.title = title;
        this.banner = banner;
        this.km = km;
        this.la = la;
        this.lo = lo;
        this.myshop = myshop;
    }
}
